    private boolean validateDate(String date) {
        final Pattern pattern = Pattern.compile("(0[1-9]|1[0-9]|2[0-9]|3[0-1]|[1-9])/(0[1-9]|1[0-2]|[1-9])/([0-9]{4})");
        final Matcher matcher = pattern.matcher(date);
        return matcher.find();
    }

    private boolean deadlineDateHasPassed(String date) {
        final SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");

        final Calendar today = Calendar.getInstance();
        final Calendar deadline = Calendar.getInstance();

        try {
            deadline.setTime(dateFormat.parse(date));
            today.setTime(dateFormat.parse(dateFormat.format(today.getTime())));

        } catch (ParseException e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }

        return today.compareTo(deadline) <= 0;
    }    
