    private void jLabelTooBarSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelTooBarSaveMouseClicked
        // TODO add your handling code here:        
        if ("".equals(jTextFieldName.getText().replace(" ", ""))) {
            JOptionPane.showMessageDialog(rootPane, "Name field is required");
            return;
        }

        final String deadline = jFormattedTextFieldDeadline.getText();
        // Valida a data do prazo final
        if (!validateDate(deadline)) {
            JOptionPane.showMessageDialog(rootPane, "Invalid date");
            return;
        }

        // Verifica se o prazo final informado é uma data mais antiga que a data atual
        if (!deadlineDateHasPassed(deadline)) {
            JOptionPane.showMessageDialog(rootPane, "Deadline date has passed");
            return;
        }

        // Verifica se a quantidade de caracteres informada no campo name é maior que 50
        final String name = jTextFieldName.getText().trim().replaceAll("\\s+", " ");
        final int len_name = name.length();
        if (len_name > 50) {
            JOptionPane.showMessageDialog(rootPane, "Name field can only contain 50 characters");
            return;
        }

        // Verifica se a quantidade de caracteres informada no campo description é maior que 255
        final String description = jTextAreaDescription.getText().trim().replaceAll("\\s+", " ");
        final int len_description = description.length();
        if (len_description > 255) {
            JOptionPane.showMessageDialog(rootPane, "Description field can only contain 255 characters");
            return;
        }

        // Verifica se a quantidade de caracteres informada no campo notes é maior que 255
        final String notes = jTextAreaNotes.getText().trim().replaceAll("\\s+", " ");
        final int len_notes = notes.length();
        if (len_notes > 255) {
            JOptionPane.showMessageDialog(rootPane, "Notes field can only contain 255 characters");
            return;
        }

        try {
            final SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
            // Cria uma nova tarefa
            final Task task = new Task();
            task.setIdProject(project.getId());
            task.setName(name);
            task.setDescription(description);
            task.setNotes(notes);
            task.setIsCompleted(false);
            task.setDeadline(dateFormat.parse(deadline));

            // Salva a tarefa no banco de dados
            taskController.save(task);

            // Mensagemm de sucesso
            JOptionPane.showMessageDialog(rootPane, "Job successfully saved");

            // Fecha a janela automaticamente
            this.dispose();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_jLabelTooBarSaveMouseClicked

